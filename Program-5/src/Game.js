	checkRoomOverlaps: function(x,y, w, h) {
		
		var overlap = false;
		for(var rm = 0; rm < this.Rooms.length; rm++)
		{
		if (( x < this.Rooms[rm].x2)  &&  ((x + w)  >  this.Rooms[rm].x1) &&
		   (y  <  this.Rooms[rm].y2)  &&( (y+h)  >  this.Rooms[rm].y1) )
		   {
			   return overlap = true;
		   }

		}
		return overlap
		
	},

